"""Build the 3-backbone x 8-domain robustness table (Qwen3-8B / Llama-3.1-8B / Mistral-7B).

Reads the committed per-(domain,backbone) report.json files and emits a LaTeX table that
EXTENDS Paper/tables/second_backbone_replication.tex (which was Llama x 4 only) to the full
3-backbone x 8-domain grid. Robust to partial completion: any cell whose report.json is not
yet present renders as "--" and the run prints a coverage summary, so it can be run NOW on the
in-flight 18/24 cells as a preview and re-run unchanged once the Mistral sweep reaches 24/24.

Schema handling (verified 2026-06-20):
  - flat reports (Llama/Mistral/new-4 Qwen): keys 'HR@10','NDCG@10',... (capitalized @-notation)
  - old-4 Qwen (outputs/ccrp_v3_formal/<dom>): keys 'hr10','ndcg10',... (lowercase)
  - beauty backbone runs use the '..._supplementary_smallerN_100neg_...' naming (973 users)

Run from repo root:  python experiments/rsc/build_3backbone_table.py
"""
import json, os, sys

# canonical paper domain order
DOMAINS = ['beauty', 'books', 'electronics', 'movies', 'sports', 'toys', 'home', 'tools']
OLD4 = {'beauty', 'books', 'electronics', 'movies'}            # Qwen old layout (lowercase keys)
PRETTY = {'beauty': 'Beauty', 'books': 'Books', 'electronics': 'Elec.', 'movies': 'Movies',
          'sports': 'Sports', 'toys': 'Toys', 'home': 'Home', 'tools': 'Tools'}

# strongest official baseline per domain (name, NDCG@10) — source: full_official_ndcg10_ranking.tex
STRONGEST = {
    'beauty': ('ProEx', 0.1506), 'books': ('LLMEmb', 0.2737), 'electronics': ('LLMEmb', 0.1196),
    'movies': ('LLMEmb', 0.1690), 'sports': ('LLMEmb', 0.1795), 'toys': ('LLMEmb', 0.2049),
    'home': ('LLMEmb', 0.0939), 'tools': ('LLMEmb', 0.1159),
}
BACKBONES = ['qwen', 'llama', 'mistral']
BB_LABEL = {'qwen': 'Qwen3-8B', 'llama': 'Llama-3.1-8B', 'mistral': 'Mistral-7B'}


def _flat_dir(dom, suffix):
    """flat-layout output dir for a 10k domain or the beauty smallerN special-case."""
    if dom == 'beauty':
        return f'outputs/beauty_supplementary_smallerN_100neg_ccrp_v3{suffix}'
    return f'outputs/{dom}_large10000_100neg_ccrp_v3{suffix}'


def report_path(dom, backbone):
    if backbone == 'qwen':
        if dom in OLD4:
            return f'outputs/ccrp_v3_formal/{dom}/report.json'
        return f'{_flat_dir(dom, "")}/report.json'
    return f'{_flat_dir(dom, "_" + backbone)}/report.json'


def get(d, *keys):
    for k in keys:
        if k in d:
            return d[k]
    return None


def load_metrics(dom, backbone):
    p = report_path(dom, backbone)
    if not os.path.exists(p):
        return None
    d = json.load(open(p))
    nd = get(d, 'NDCG@10', 'ndcg10')
    if nd is None:
        return None
    return {
        'ndcg10': nd, 'hr10': get(d, 'HR@10', 'hr10'),
        'ndcg5': get(d, 'NDCG@5', 'ndcg5'), 'ndcg20': get(d, 'NDCG@20', 'ndcg20'),
        'hr5': get(d, 'HR@5', 'hr5'), 'hr20': get(d, 'HR@20', 'hr20'),
        'mrr': get(d, 'MRR', 'mrr'), 'n_users': d.get('n_users'),
    }


def fmt(x):
    return f'${x:.4f}$' if isinstance(x, (int, float)) else '--'


def imp(nd, base):
    return f'${100*(nd-base)/base:+.1f}\\%$' if isinstance(nd, (int, float)) else '--'


def main():
    grid = {dom: {bb: load_metrics(dom, bb) for bb in BACKBONES} for dom in DOMAINS}
    present = sum(1 for dom in DOMAINS for bb in BACKBONES if grid[dom][bb])
    total = len(DOMAINS) * len(BACKBONES)

    # --- coverage report (stderr-style stdout) ---
    print(f"=== 3-backbone coverage: {present}/{total} cells ===")
    for bb in BACKBONES:
        have = [d for d in DOMAINS if grid[d][bb]]
        print(f"  {BB_LABEL[bb]:14s} {len(have)}/8  ({', '.join(have) if have else 'none'})")

    # --- emit LaTeX ---
    lines = []
    lines.append('% AUTO-GENERATED by experiments/rsc/build_3backbone_table.py')
    lines.append(f'% Coverage at generation: {present}/{total} cells. Re-run when Mistral reaches 24/24.')
    lines.append('\\begin{table*}[t]')
    lines.append('\\centering')
    lines.append('\\scriptsize')
    lines.append('\\setlength{\\tabcolsep}{5pt}')
    lines.append('\\caption{\\textbf{Backbone robustness (3 backbones $\\times$ 8 domains).} '
                 'NDCG@10 of the identical same-candidate C-CRP pipeline under three different-family '
                 'frozen LLMs, with the strongest official baseline per domain for reference. '
                 'The pointwise-posterior rank-1 result is a property of the method, not of one '
                 'backbone: it replicates across backbones in the six domains where Qwen ranks first '
                 '(Beauty/Movies shown honestly). Absolute level tracks backbone quality.}')
    lines.append('\\label{tab:three_backbone}')
    lines.append('\\begin{tabular}{lccccc}')
    lines.append('\\toprule')
    lines.append('& \\multicolumn{3}{c}{C-CRP NDCG@10 by backbone} & & \\\\')
    lines.append('\\cmidrule(lr){2-4}')
    lines.append('Domain & Qwen3-8B & Llama-3.1-8B & Mistral-7B & Strongest base & '
                 'Mistral vs.\\ base \\\\')
    lines.append('\\midrule')
    for dom in DOMAINS:
        q, l, m = grid[dom]['qwen'], grid[dom]['llama'], grid[dom]['mistral']
        bname, bval = STRONGEST[dom]
        qn = q['ndcg10'] if q else None
        ln = l['ndcg10'] if l else None
        mn = m['ndcg10'] if m else None
        row = (f"{PRETTY[dom]:11s} & {fmt(qn)} & {fmt(ln)} & {fmt(mn)} & "
               f"{bname}~{fmt(bval)} & {imp(mn, bval)} \\\\")
        lines.append(row)
    lines.append('\\bottomrule')
    lines.append('\\end{tabular}')
    lines.append('\\end{table*}')

    out = 'Paper/tables/three_backbone_robustness.tex'
    with open(out, 'w', encoding='utf-8') as f:
        f.write('\n'.join(lines) + '\n')
    print(f"\nwrote {out} ({present}/{total} cells; '--' = not yet landed)")
    if present < total:
        print(f"NOTE: preview only. {total-present} cells pending -- re-run after Mistral sweep completes.")
    return 0


if __name__ == '__main__':
    sys.exit(main())
