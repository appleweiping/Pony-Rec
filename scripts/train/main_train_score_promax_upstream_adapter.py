def _l2_normalize(matrix: Any, np: Any) -> Any:
    denom = np.linalg.norm(matrix, axis=1, keepdims=True)
    return (matrix / np.maximum(denom, 1.0e-8)).astype(np.float32)


def _make_profile_retrieval_mat(
    *,
    user_profiles: Any,
    item_profiles: Any,
    train_sequences: dict[int, list[int]],
    user_count: int,
    item_count: int,
    topk: int,
    block_users: int,
    coo_matrix: Any,
    np: Any,
) -> tuple[Any, dict[str, Any]]:
    topk = max(1, int(topk))
    block_users = max(1, int(block_users))
    item_norm = _l2_normalize(item_profiles, np)
    user_norm = _l2_normalize(user_profiles, np)
    rows: list[int] = []
    cols: list[int] = []
    for start in range(0, user_count, block_users):
        end = min(start + block_users, user_count)
        sims = user_norm[start:end] @ item_norm.T
        for offset, scores in enumerate(sims):
            user_idx = start + offset
            seen = train_sequences.get(user_idx, [])
            if seen:
                valid_seen = [idx for idx in seen if 0 <= idx < item_count]
                scores[valid_seen] = -np.inf
            if topk >= item_count:
                picked = np.argsort(-scores)
            else:
                candidate = np.argpartition(-scores, topk - 1)[:topk]
                picked = candidate[np.argsort(-scores[candidate])]
            for item_idx in picked[:topk]:
                if np.isfinite(scores[item_idx]):
                    rows.append(user_idx)
                    cols.append(int(item_idx))
    data = np.ones((len(rows),), dtype=np.float32)
    mat = coo_matrix((data, (rows, cols)), shape=(user_count, item_count), dtype=np.float32)
    summary = {
        "profile_retrieval_bridge": "qwen3_pca64_user_item_profile_topk_latent_interactions",
        "rag_topk": topk,
        "rag_block_users": block_users,
        "latent_interaction_edges": int(len(rows)),
        "latent_interaction_users": int(len(set(rows))),
        "latent_interaction_items": int(len(set(cols))),
        "note": (
            "ProMax's official LightGCN_promax objective is preserved. The required "
            "new_trn_rag_mat.pkl is generated by Qwen3-derived PCA64 user-profile to "
            "item-profile retrieval over the same-candidate domain, excluding observed "
            "training interactions."
        ),
    }
    return mat, summary


def _write_profile_data(
    *,
    adapter_dir: Path,
    repo_dir: Path,
    dataset_name: str,
    trn_mat: Any,
    val_mat: Any,
    tst_mat: Any,
    new_trn_rag_mat: Any,
    user_profiles: Any,
    item_profiles: Any,
    item_embedding_source_path: Path,
    manifest: dict[str, Any],
) -> tuple[Path, Path]:
    handled_dir = adapter_dir / "promax" / "handled"
    handled_dir.mkdir(parents=True, exist_ok=True)
    for name, payload in {
        "trn_mat.pkl": trn_mat,
        "val_mat.pkl": val_mat,
        "tst_mat.pkl": tst_mat,
        "new_trn_rag_mat.pkl": new_trn_rag_mat,
        "usr_emb_np.pkl": user_profiles,
        "itm_emb_np.pkl": item_profiles,
    }.items():
        with (handled_dir / name).open("wb") as fh:
            pickle.dump(payload, fh)

    manifest = {
        **manifest,
        "adapter_profile_dir": str(handled_dir),
        "item_embedding_source_path": str(item_embedding_source_path),
        "user_profile_shape": list(user_profiles.shape),
        "item_profile_shape": list(item_profiles.shape),
    }
    (handled_dir / "promax_profile_bridge_manifest.json").write_text(
        json.dumps(manifest, indent=2, ensure_ascii=False) + "\n",
        encoding="utf-8",
    )

    official_data_dir = repo_dir / "data" / dataset_name
    if official_data_dir.exists() or official_data_dir.is_symlink():
        resolved = official_data_dir.resolve() if official_data_dir.exists() else official_data_dir
        if repo_dir.resolve() not in resolved.parents and resolved != handled_dir.resolve():
            raise RuntimeError(f"Refusing to replace unexpected ProRec data directory: {official_data_dir}")
        if official_data_dir.is_symlink() or official_data_dir.is_file():
            official_data_dir.unlink()
        else:
            shutil.rmtree(official_data_dir)
    official_data_dir.parent.mkdir(parents=True, exist_ok=True)
    try:
        official_data_dir.symlink_to(handled_dir.resolve(), target_is_directory=True)
    except Exception:
        shutil.copytree(handled_dir, official_data_dir)
    return handled_dir, official_data_dir


def _make_torch_adj_from_user_item_mat(
    mat: Any,
    *,
    user_count: int,
    item_count: int,
    sp: Any,
    np: Any,
    torch: Any,
    device: Any,
) -> Any:
    a = sp.csr_matrix((user_count, user_count))
    b = sp.csr_matrix((item_count, item_count))
    adj = sp.vstack([sp.hstack([a, mat]), sp.hstack([mat.transpose(), b])])
    adj = (adj != 0) * 1.0
    adj = _normalize_adj(adj, sp, np)
    idxs = torch.from_numpy(np.vstack([adj.row, adj.col]).astype(np.int64))
    vals = torch.from_numpy(adj.data.astype(np.float32))
    shape = torch.Size(adj.shape)
    return torch.sparse_coo_tensor(idxs, vals, shape).coalesce().to(device)


class _DataHandler:
    def __init__(self, *, torch_adj: Any, user_count: int, item_count: int, sp: Any, np: Any, torch: Any, device: Any) -> None:
        self.torch_adj = torch_adj
        self.user_count = user_count
        self.item_count = item_count
        self.sp = sp
        self.np = np
        self.torch = torch
        self.device = device

    def _make_torch_adj(self, mat: Any) -> Any:
        return _make_torch_adj_from_user_item_mat(
            mat,
            user_count=self.user_count,
            item_count=self.item_count,
            sp=self.sp,
            np=self.np,
            torch=self.torch,
            device=self.device,
        )


